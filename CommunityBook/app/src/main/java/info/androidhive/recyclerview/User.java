public class User {
    long id;
    String fname;
    String lname;
    int age;
    int roleNo;

    public User(long id, String fname, String lname, int age, int roleNo) {
        this.id = id;
        this.fname = fname;
        this.lname = lname;
        this.age = age;
        this.roleNo = roleNo;
    }



}
